public class Value {
    public VideoFile videoFile;

    public Value()
    {}

    public Value(VideoFile videoFile)
    {
        this.videoFile=videoFile;
    }

    //get-set
    public VideoFile getVideoFile()
    {
        return videoFile;
    }

    public void setVideoFile(VideoFile videoFile)
    {
        this.videoFile=videoFile;
    }

}
